     */
    public function store(Request $request)
    {

        if ($request->filled(('title'))) {
            $title = $request->input(('title'));

            $task = new Task;
            $task -> title = $title;
            $task -> save();

            return redirect()->route('tasks.index');
        }else{
            return redirect()
                ->route('tasks.create')
                ->with('warning', 'Você não preencheu o titulo');
        }
    }

